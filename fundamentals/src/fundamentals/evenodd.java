package fundamentals;

public class evenodd {

		public static void main(String[] args) {
			int n;
	        n=Integer.parseInt(args[0]);
	        if(n%2== 0)
	        {
	        	
	            System.out.println("The given number "+n+" is even");
	        }
	        else
	        {
	            System.out.println("The given number "+n+" is odd");
	        }

		}

}
